#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*converted_n;
	int		len;

	if (!(fd < 0))
	{
		converted_n = ft_itoa(n);
		len = ft_strlen(converted_n);
		while (len-- > 0 && *converted_n != '\0')
		{
			write(fd, converted_n, 1);
			converted_n++;
		}
	}
}

