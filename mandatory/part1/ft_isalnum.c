int	ft_isalnum(unsigned char c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
	{
		return 1;
	}
	else if (c >= '0' && c <= '9')
	{
		return 1;
	}
	else
	{
		return 0;
	}
}
