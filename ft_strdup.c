#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		lens;
	char	*str;
	int		i;

	i = 0;
	lens = ft_strlen(s1) + 1;
	str = (char *)malloc(lens * sizeof(char));
	if (str == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}


